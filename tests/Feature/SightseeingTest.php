    public function user_with_permission_can_sort_sightseeings($auth)
    {
        $auth = $auth == 0 ? $this->admin->user : $this->employee_read->user;
        for ($z=0;$z<10;$z++){
            $all_count = 10;
            $sightseeings = factory(Sightseeing::class, $all_count)->create();
            $param = 'name';
            $url_query = '?order[0][dir]=asc&order[0][column]=0&columns[0][data]=' . $param;

            $response = $this->actingAs($auth)->get($this->index_route.$url_query);
            $response->assertOk();

            $sightseeings_from_view = $this->getDataFromDatatableResponse($response);
            $this->assertNotNull($sightseeings_from_view);
            $this->assertGreaterThan(0, count($sightseeings_from_view));

            $sightseeings = $sightseeings_from_view;
            for ($i=0;$i<count($sightseeings);$i++){
                if($i > 0){
                    if (stripos($param, '.') !== false) {
                        $parts = explode('.', $param);
                        $this->greaterThanOrEqual($sightseeings->get($i - 1)->{$parts[0]}->{$parts[1]}, $sightseeings->get($i)->{$parts[0]}->{$parts[1]});
                    } else {
                        $this->greaterThanOrEqual($sightseeings->get($i - 1)->{$param}, $sightseeings->get($i)->{$param});
                    }
                }
            }
        }
    }

