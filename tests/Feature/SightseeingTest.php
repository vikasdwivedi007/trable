    /**
     * @test
     * @dataProvider authUserProvider
     */
    public function user_with_permission_can_search_sightseeing($auth)
    {
        $auth = $auth == 0 ? $this->admin->user : $this->employee_read->user;
        $loops = 13;
        $can_search_by = (new Sightseeing())->can_search_by;
        $can_search_by = array_map(function ($item) {
            return str_replace('sightseeings.', '', $item);
        }, $can_search_by);
        for ($i=0;$i<$loops;$i++){
            $sightseeings = factory(Sightseeing::class, 20)->create();

            $rand_rest = $sightseeings->toArray()[rand(0,9)];
            $rand_name = $rand_rest['name'];
            $rand_word = Arr::random(explode(' ', $rand_name));
            $url_query = '?search[value]=' . $rand_word;

            $response = $this->actingAs($auth)->get($this->index_route.$url_query);
            $response->assertOk();
            $sightseeings_from_view = $this->getDataFromDatatableResponse($response);
            $this->assertNotNull($sightseeings_from_view);
            $this->assertGreaterThan(0, count($sightseeings_from_view));

            //assert searched name exists in the result
            foreach($sightseeings_from_view as $sightseeing){
                $sightseeing = Sightseeing::find($sightseeing->id);
                $found = 0;
                foreach ($can_search_by as $key) {
                    if (stripos($key, '.') !== false) {
                        $parts = explode('.', $key);
                        if (stripos($sightseeing->{$parts[0]}->{$parts[1]}, $rand_word) !== false) {
                            $found++;
                        }
                    } elseif (stripos($sightseeing->{$key}, $rand_word) !== false) {
                        $found++;
                    }
                }
                $this->assertGreaterThan(0, $found);
            }
        }
    }

